
public class Quad {
		
		private Double a,b,c;
		public Quad(Double x,Double y,Double z)
		{   a=x;
			b=y;
			c=z;
		}
		public void setA(Double x) 
		{ a=x; }
		public void setB(Double y)
		{ b=y; }
		public void setC(Double z)
		{ c=z; }
		public Double getA()
		{ return a; }
		public Double getB()
		{ return b; }
		public Double getC()
		{ return c; }
}






  
